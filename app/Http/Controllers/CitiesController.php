    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $cityQuery = City::query();

        $sort = explode(':', $request->get('sort', 'id:desc'));
        $cityQuery->orderBy($sort[0], $sort[1]);

        // filters
        if ($request->has('name')) {
            $cityQuery
                ->where('en_name', 'like', '%' . $request->get('name') . '%')
                ->orWhere('ar_name', 'like', '%' . $request->get('name') . '%');
        }

        // end of filters
        $cities = $cityQuery->paginate();
        return new CityResourceCollection($cities);
    }

